package com.cmy.zujuan.pojo;

/**
 * 难度类型
 * 1 简单 2 一般 3 中等 4 困难 5 特难
 */
public class DifficultyType {

    private String id;

    private String name;

    // Getters and Setters

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}